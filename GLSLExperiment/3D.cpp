/*Xử lý view*/
void view_manager()
{
	eye = vec4(eye.x + view_translate.x, eye.y + view_translate.y, eye.z + view_translate.z, eye.w );
	view = LookAt(eye, at, up);
	glUniformMatrix4fv(view_loc, 1, GL_TRUE, view);
}

/* Tính toán màu sắc vật phẩm */
void updateProductColors(void)  
{
	ambient_product = light_ambient * material_ambient;
	diffuse_product = light_diffuse * material_diffuse;
	specular_product = light_specular * material_specular;
}

/*Update chất liệu cho từng object*/
void objectMaterialRender(vec4 material[3])  
{
	material_diffuse = material[0];
	material_ambient = material[1];
	material_specular = material[2];
	material_shininess = 100.0f;

	updateProductColors();

	glUniform4fv(ambi, 1, ambient_product);
	glUniform4fv(diff, 1, diffuse_product);
	glUniform4fv(spec, 1, specular_product);
	glUniform1f(shin, material_shininess);
}

